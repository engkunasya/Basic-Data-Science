'''
Write a Python program that does the following:
Prompts the user to enter two numbers. Stores these numbers in two variables.
Performs and prints the results of addition, subtraction, multiplication, and division of these two numbers.
'''

numFirst = int(input("Please enter your first number: "))
numSecond = int(input("Please enter your second number: "))

x = numFirst
y = numSecond

print("The addition of first and second number: ", x + y)
print("The subtraction of first and second number:", x - y)
print("The multiplication of first and second number:", x * y)
print("The division of first and second number:", x / y)